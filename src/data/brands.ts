// Helper functions
export const getBrandBySlug = (slug: string): Brand | undefined => {
  return brands.find(b => b.slug === slug);
};

export const getBrandById = (id: string): Brand | undefined => {
  return brands.find(b => b.id === id);
};

export const getBrandByName = (name: string): Brand | undefined => {
  return brands.find(b => b.name.toLowerCase() === name.toLowerCase());
};

export const getBrandsByCategory = (category: Brand["category"]): Brand[] => {
  return brands.filter(b => b.category === category);
};
